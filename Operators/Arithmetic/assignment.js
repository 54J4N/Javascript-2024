//The Assignment operation ,represent by the equals = sign.
let x = 10;
let y = 5;

//add
x += y; // x = x + y;
console.log(x);
//sub
x -= y; //x=x-y;
console.log(x);
//mul
x *= y; //x=x*y;
console.log(x);
//div
x /= y; //x=x/y;
console.log(x);
//remainder
x %= y; //x=x%y;
console.log(x);